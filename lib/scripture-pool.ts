export function getAutoScripture(date: Date = new Date()): ScriptureEntry {
  const start = new Date(date.getFullYear(), 0, 0)
  const diff  = date.getTime() - start.getTime()
  const dayOfYear = Math.floor(diff / 86400000)
  return SCRIPTURE_POOL[dayOfYear % SCRIPTURE_POOL.length]
}
